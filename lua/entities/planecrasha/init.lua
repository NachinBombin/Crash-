   -- Wait until the boom animation has visually settled (~2.5s in).
   -- GetBonePosition(0) returns the root bone world position, which follows
   -- the animation transform -- unlike GetPos() which stays at spawn origin.
   timer.Simple( 17.5, function()
      if not IsValid( fuseA ) then return end
      local bonePos, _ = fuseA:GetBonePosition( 0 )
      -- Fallback: if bone returns nil or zero vector, drop a traceline from
      -- spawnpos downward to find the ground directly below.
      if not bonePos or bonePos == Vector(0,0,0) then
         local tr = util.TraceLine({
            start  = spawnpos + Vector(0,0,100),
            endpos = spawnpos - Vector(0,0,2000),
            mask   = MASK_SOLID_BRUSHONLY
         })
         bonePos = tr.Hit and tr.HitPos or spawnpos
      end
      net.Start( "PlaneCrashEffects" )
         net.WriteVector( bonePos )
      net.Broadcast()
   end )

   timer.Simple( 20.5, function() util.ScreenShake( spawnpos, 4, 100, 4, 16000 ) end )
   timer.Simple( 23.0, function() util.ScreenShake( spawnpos, 4, 100, 4, 16000 ) end )
   timer.Simple( 24.0, function() util.ScreenShake( spawnpos, 4, 100, 4, 16000 ) end )
   timer.Simple( 26.0, function() util.ScreenShake( spawnpos, 4, 100, 4, 16000 ) end )
   timer.Simple( 14.96, function() self:Remove() end )
   timer.Simple( gonetimer, function() fuseA:Remove() end )
   timer.Simple( gonetimer, function() fuseB:Remove() end )
   timer.Simple( gonetimer, function() fuseC:Remove() end )
   timer.Simple( gonetimer, function() fuseD:Remove() end )
   timer.Simple( gonetimer, function() fuseE:Remove() end )
   timer.Simple( gonetimer, function() fuseF:Remove() end )
   timer.Simple( gonetimer, function() fuseG:Remove() end )
   timer.Simple( gonetimer, function() fuseH:Remove() end )
   timer.Simple( gonetimer, function() fuseI:Remove() end )
   timer.Simple( gonetimer, function() fuseJ:Remove() end )
   timer.Simple( gonetimer, function() fuseK:Remove() end )
   timer.Simple( gonetimer, function() fuseL:Remove() end )
end

function ENT:Think()

	self:NextThink( CurTime() )
	return true
end
