   timer.Simple( 14.95, function() fuseE:SetPos( spawnpos ) end )
   timer.Simple( 14.95, function() fuseE:SetAngles( spawnangles ) end )
   timer.Simple( 14.96, function() fuseE:SetSequence( "boom" ) end )
   timer.Simple( 14.95, function() fuseE:Spawn() end )
