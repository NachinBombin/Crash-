   timer.Simple( 14.95, function() fuseA:SetPos( spawnpos ) end )
   timer.Simple( 14.95, function() fuseA:SetAngles( spawnangles ) end )
   timer.Simple( 14.96, function() fuseA:SetSequence( "boom" ) end )
   timer.Simple( 14.95, function() fuseA:Spawn() end )
